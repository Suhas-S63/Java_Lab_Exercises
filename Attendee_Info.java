    public void Load()
    {
        try {
            pat=con.prepareStatement("select * from attendee");
            ResultSet rs=pat.executeQuery();
            ResultSetMetaData rss=rs.getMetaData();
            int c;
            c=rss.getColumnCount();
            def=(DefaultTableModel)jTable2.getModel();
            def.setRowCount(0);
            while(rs.next())
            {
                Vector v=new Vector();
                for(int i=0;i<c;i++)
                {
                    v.add(rs.getString("id"));
                     v.add(rs.getString("full_name"));
                      v.add(rs.getString("email"));
                       v.add(rs.getString("contact"));
                       v.add(rs.getString("country"));
                }
                def.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Test4.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

