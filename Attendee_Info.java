    private void Guest_Search(Connection con,String search_string) throws SQLException
    {
        try
        {
            pat = con.prepareStatement("select * from attendee where id = ? or full_name like ? or country like ?");
            pat.setString(1, search_string);
            pat.setString(2,"%" + search_string + "%");
            pat.setString(3,"%" + search_string + "%");
            
            ResultSet rs = pat.executeQuery();
            while (rs.next()) {
                int id = rs.getInt("id");
                String fullName = rs.getString("full_name");
                String email = rs.getString("email");
                String contactNumber = rs.getString("contact");
                String country = rs.getString("country");

                JOptionPane.showMessageDialog(this,"ID: " + id + ", Full Name: " + fullName + ", Email: " + email + ", Contact Number: " + contactNumber + ", Country: " + country);
            }
        }
        catch(SQLException ex)
        {
           Logger.getLogger(Attendee_Info.class.getName()).log(Level.SEVERE,null,ex); 
        }
    }
    
    private static void generateAttendeeStatistics(Connection con) throws SQLException
    {
        try
        {
            CallableStatement cs = con.prepareCall("{ CALL getAttendeeStatitics() }");
            ResultSet rs = cs.executeQuery();
            
            String statsMessage = "Attendee Statistics:\n";
            while(rs.next())
            {
                String country = rs.getString("country");
                int attendeeCount = rs.getInt("attendeeCount");
                
                statsMessage += "Country: " + country + ", Attendee Count: " + attendeeCount + "\n";
            }    
            JOptionPane.showMessageDialog(null, statsMessage, "Attendee Statistics", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException ex)
        {
           Logger.getLogger(Attendee_Info.class.getName()).log(Level.SEVERE,null,ex); 
        }
    }
    
    

